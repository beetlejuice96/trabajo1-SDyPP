package tp1_punto2.punto2;

public class MensajePrueba {
	int id;
	String mensaje;
	
	public MensajePrueba(int id, String mensaje) {
		super();
		this.id = id;
		this.mensaje = mensaje;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}


	
	

}

